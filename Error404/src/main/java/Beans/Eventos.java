package beans;

public class Eventos {
    private int eventId;
    private int userId;
    private String nombreEvento;
    private String fechaEvento;
    private String horaEvento;
    private int lugarEventoId;
    private String entrada;
    private String descripcionEvento;
    private String artistasProveedores;
    private String razonEvento;
    private String fechaCreacion;
    private String estadoEvento;

    public int getEventId() {
        return eventId;
    }

    public void setEventId(int eventId) {
        this.eventId = eventId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getNombreEvento() {
        return nombreEvento;
    }

    public void setNombreEvento(String nombreEvento) {
        this.nombreEvento = nombreEvento;
    }

    public String getFechaEvento() {
        return fechaEvento;
    }

    public void setFechaEvento(String fechaEvento) {
        this.fechaEvento = fechaEvento;
    }

    public String getHoraEvento() {
        return horaEvento;
    }

    public void setHoraEvento(String horaEvento) {
        this.horaEvento = horaEvento;
    }

    public int getLugarEventoId() {
        return lugarEventoId;
    }

    public void setLugarEventoId(int lugarEventoId) {
        this.lugarEventoId = lugarEventoId;
    }

    public String getEntrada() {
        return entrada;
    }

    public void setEntrada(String entrada) {
        this.entrada = entrada;
    }

    public String getDescripcionEvento() {
        return descripcionEvento;
    }

    public void setDescripcionEvento(String descripcionEvento) {
        this.descripcionEvento = descripcionEvento;
    }

    public String getArtistasProveedores() {
        return artistasProveedores;
    }

    public void setArtistasProveedores(String artistasProveedores) {
        this.artistasProveedores = artistasProveedores;
    }

    public String getRazonEvento() {
        return razonEvento;
    }

    public void setRazonEvento(String razonEvento) {
        this.razonEvento = razonEvento;
    }

    public String getFechaCreacion() {
        return fechaCreacion;
    }

    public void setFechaCreacion(String fechaCreacion) {
        this.fechaCreacion = fechaCreacion;
    }

    public String getEstadoEvento() {
        return estadoEvento;
    }

    public void setEstadoEvento(String estadoEvento) {
        this.estadoEvento = estadoEvento;
    }
// Getters y Setters para todos los campos
}

