package Beans;

public class Fotos {
    private int fotoId;
    private String urlFoto;
    private String fechaSubida;
    private Integer usuarioId;
    private Integer publicacionId;
    private Integer lugarEventoId;
    private Integer eventoId;
    private Integer mascotaId;
    private Integer denunciaId;
    private Integer temporalId;

    public int getFotoId() {
        return fotoId;
    }

    public void setFotoId(int fotoId) {
        this.fotoId = fotoId;
    }

    public String getUrlFoto() {
        return urlFoto;
    }

    public void setUrlFoto(String urlFoto) {
        this.urlFoto = urlFoto;
    }

    public String getFechaSubida() {
        return fechaSubida;
    }

    public void setFechaSubida(String fechaSubida) {
        this.fechaSubida = fechaSubida;
    }

    public Integer getUsuarioId() {
        return usuarioId;
    }

    public void setUsuarioId(Integer usuarioId) {
        this.usuarioId = usuarioId;
    }

    public Integer getPublicacionId() {
        return publicacionId;
    }

    public void setPublicacionId(Integer publicacionId) {
        this.publicacionId = publicacionId;
    }

    public Integer getLugarEventoId() {
        return lugarEventoId;
    }

    public void setLugarEventoId(Integer lugarEventoId) {
        this.lugarEventoId = lugarEventoId;
    }

    public Integer getEventoId() {
        return eventoId;
    }

    public void setEventoId(Integer eventoId) {
        this.eventoId = eventoId;
    }

    public Integer getMascotaId() {
        return mascotaId;
    }

    public void setMascotaId(Integer mascotaId) {
        this.mascotaId = mascotaId;
    }

    public Integer getDenunciaId() {
        return denunciaId;
    }

    public void setDenunciaId(Integer denunciaId) {
        this.denunciaId = denunciaId;
    }

    public Integer getTemporalId() {
        return temporalId;
    }

    public void setTemporalId(Integer temporalId) {
        this.temporalId = temporalId;
    }
}

